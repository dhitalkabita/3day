public class nestedLoop {
    public static void main(String[] args) {
        //nested loop = A loop inside another loop.
        // used often with matrics or DSA
        for (int i = 0; i < 10; i++) {
            System.out.print(i + " ");

        }
       System.out.println();
        for (int i = 0; i < 10; i++) {
            System.out.print(i + " ");
        }
       System.out.println();
        for (int i = 0; i < 10; i++) {
            System.out.print(i + " ");
        }
    }
}